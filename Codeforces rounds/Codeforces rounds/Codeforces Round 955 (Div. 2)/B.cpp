ull solve(ull x, ull y, ull k) {

  while (k > 0 && x > 1) {
    ull d = y - x % y;
    if (k < d) {
      return x + k;
    }

    k -= d;
    x += d;
    while (x % y == 0) {
      x /= y;
    }
  }
  if (k == 0) {
    return x;
  }

  k %= (y - 1);

  return k + 1;
}

